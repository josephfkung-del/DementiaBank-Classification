def get_texts_for_split(split_name):
    """
    Tries to find transcript/text list inside splits[split_name].
    Works if your notebook stores text as:
        splits[split]["text"]
        splits[split]["texts"]
        splits[split]["transcripts"]
        splits[split]["utt_texts"]

    If this errors, print(splits[split].keys()) and adjust the key.
    """
    s = splits[split_name]

    possible_keys = ["text", "texts", "transcript", "transcripts", "utt_texts"]

    for key in possible_keys:
        if key in s:
            return list(s[key])

    raise ValueError(
        f"Could not find transcript text in splits['{split_name}']. "
        f"Available keys are: {list(s.keys())}. "
        "Change get_texts_for_split() to use the correct text key."
    )


