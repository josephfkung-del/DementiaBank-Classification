def make_cookie_semantic_features(text_emb, phrase_emb):
    """
    Computes similarity between each transcript and each Cookie Theft phrase.

    Features include:
        - similarity to each phrase
        - mean similarity
        - max similarity
        - std similarity
        - top-3 average similarity
        - coverage above thresholds
    """

    # cosine similarity because embeddings are normalized
    sims = text_emb @ phrase_emb.T

    mean_sim = sims.mean(axis=1, keepdims=True)
    max_sim = sims.max(axis=1, keepdims=True)
    min_sim = sims.min(axis=1, keepdims=True)
    std_sim = sims.std(axis=1, keepdims=True)

    sorted_sims = np.sort(sims, axis=1)
    top3_mean = sorted_sims[:, -3:].mean(axis=1, keepdims=True)
    top5_mean = sorted_sims[:, -5:].mean(axis=1, keepdims=True)

    coverage_035 = (sims > 0.35).mean(axis=1, keepdims=True)
    coverage_045 = (sims > 0.45).mean(axis=1, keepdims=True)
    coverage_055 = (sims > 0.55).mean(axis=1, keepdims=True)

    X = np.hstack([
        sims,
        mean_sim,
        max_sim,
        min_sim,
        std_sim,
        top3_mean,
        top5_mean,
        coverage_035,
        coverage_045,
        coverage_055
    ])

    return X, sims


