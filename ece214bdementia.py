def session_probs_to_df(model_name, session_output):
    """
    Converts get_session_probs output into a DataFrame.
    Automatically detects:
        - session IDs: strings like '022-2'
        - labels: 0/1 integers
        - probabilities: floats between 0 and 1
    """

    if not isinstance(session_output, (tuple, list)):
        raise ValueError(f"Expected tuple/list, got {type(session_output)}")

    if len(session_output) != 3:
        raise ValueError(f"Expected 3 outputs, got {len(session_output)}")

    parts = []

    for item in session_output:
        arr = np.asarray(item).ravel()
        parts.append(arr)

    session_ids = None
    session_y = None
    session_probs = None

    for arr in parts:
        numeric_arr = pd.to_numeric(pd.Series(arr), errors="coerce").values

        if np.isnan(numeric_arr).all():
            session_ids = arr.astype(str)


        elif set(np.unique(numeric_arr)).issubset({0, 1}):
            session_y = numeric_arr.astype(int)


        elif np.all((numeric_arr >= 0) & (numeric_arr <= 1)):
            session_probs = numeric_arr.astype(float)

    if session_ids is None:
        raise ValueError("Could not detect session IDs.")

    if session_y is None:
        raise ValueError("Could not detect true labels.")

    if session_probs is None:
        raise ValueError("Could not detect probabilities.")

    df = pd.DataFrame({
        "model": [model_name] * len(session_ids),
        "session": session_ids,
        "y_true": session_y,
        "prob_AD": session_probs
    })

    df["y_pred"] = (df["prob_AD"] >= 0.5).astype(int)

    return df

