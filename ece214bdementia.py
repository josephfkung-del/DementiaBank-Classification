def extract_text_transformer_features(
    texts,
    utts,
    model_id="bert-base-uncased",
    cache_path=None,
    batch_size=16,
    max_length=128,
    pooling="cls"
):
    """
    Extract text embeddings from BERT/RoBERTa.

    texts: list of transcript strings
    utts: list of utterance IDs
    model_id: Hugging Face model name
    pooling:
        "cls"  = use [CLS] token embedding
        "mean" = mean-pool over all tokens
    """

    if cache_path is not None:
        cache_path = Path(cache_path)
        if cache_path.exists():
            print(f"Loading cached features from {cache_path}")
            z = np.load(cache_path)
            return {k: z[k] for k in z.files}

    tokenizer = AutoTokenizer.from_pretrained(model_id)
    model = AutoModel.from_pretrained(model_id).to(device)
    model.eval()

    features = {}

    for i in tqdm(range(0, len(texts), batch_size), desc=f"Extracting {model_id}"):
        batch_texts = texts[i:i + batch_size]
        batch_utts = utts[i:i + batch_size]

        inputs = tokenizer(
            batch_texts,
            padding=True,
            truncation=True,
            max_length=max_length,
            return_tensors="pt"
        )

        inputs = {k: v.to(device) for k, v in inputs.items()}

        with torch.no_grad():
            outputs = model(**inputs)

        hidden = outputs.last_hidden_state  # [batch, tokens, hidden_dim]

        if pooling == "cls":
            emb = hidden[:, 0, :]  # [CLS] token
        elif pooling == "mean":
            mask = inputs["attention_mask"].unsqueeze(-1)
            emb = (hidden * mask).sum(dim=1) / mask.sum(dim=1)
        else:
            raise ValueError("pooling must be 'cls' or 'mean'")

        emb = emb.cpu().numpy().astype(np.float32)

        for utt, vec in zip(batch_utts, emb):
            features[utt] = vec

    if cache_path is not None:
        np.savez(cache_path, **features)
        print(f"Saved {len(features)} features to {cache_path}")

    return features

